# list comprehension
'''
to start, name the list and set it equal to open brackets, then there will be three things that go in this
the value incrementer or multiplier (so value * 2 e.g), then the for loop, no need for colons,
then call the range function and have it iterate through a set of numbers
'''
odd_numbers = [value * 1 for value in range(1, 21, 3)]
print(odd_numbers)
